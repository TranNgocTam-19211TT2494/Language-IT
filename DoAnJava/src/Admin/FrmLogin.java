    public void send() {
        
        try {
            Connection con = DbKetNoi.Db.getConnection();
            PreparedStatement ps;
            ps = con.prepareStatement("SELECT * FROM user WHERE taikhoan = ? AND password = ?");

            ps.setString(1, txtUser.getText());
            ps.setString(2, String.valueOf(txtPass.getPassword()));

            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
//                FrmMain m = new FrmMain();
                nameLogin = txtUser.getText();
                new FrmMain().setVisible(true);
//                m.main(null);
                dispose();
                
            } else {
                JOptionPane.showMessageDialog(null, "Sai tên Đăng Nhập hoặc Mật Khẩu.Vui lòng kiểm tra lại !!!", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Lỗi kết nối!!");
        }
        if (CheckLogin()==true) {
            return;
        }
    }
