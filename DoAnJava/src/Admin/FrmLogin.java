    public  boolean CheckLogin(){
        Check c = new Check();
        if (!c.checkLogin(txtUser.getText())) {
            JOptionPane.showMessageDialog(this, "Bạn chưa điền tài khoản", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            txtUser.setText("");
            txtUser.requestFocus();
            return false;
        }
        return true;
    }
